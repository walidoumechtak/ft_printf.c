#include "printf.h"

void    ft_putstr(char *s)
{
    int i;

    if (s == NULL)
    {
        write(1, "(null)", 6);
        return ;
    }
    i = 0;
    while(s[i])
    {
        ft_putchar(s[i]);
        i++;
    }
}